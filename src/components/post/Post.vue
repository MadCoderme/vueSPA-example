<script setup lang="ts">
import '@/assets/common.css'
import '@/assets/post.css'
import CommentSection from './CommentSection.vue'

import { computed, onMounted, onUnmounted, ref, type Ref } from 'vue'
import { useMoreOptionsSheet } from '@/stores/moreOptions'
import { usePostsApi } from '@/stores/posts'
import { supabase } from '@/lib/supabaseClient'
import { formatText, formatTime, langCodes } from '@/lib/utils'
import type { RealtimeChannel } from '@supabase/supabase-js'
import type { PostDataType, CommentDataType } from '@/lib/types/supabaseext'

const props = defineProps<{
    item?: PostDataType
}>()

const item = computed(() => props.item)
const isLoved = ref(item.value?.loves[0]?.id ? true : false)
const commentInput = ref('')
const replyTo : Ref<null | CommentDataType> = ref(null)
const comments = ref()
let commentChannel : RealtimeChannel

const moreOptions = useMoreOptionsSheet()
const api = usePostsApi()

onMounted(() => {
    if (item.value?.id && item.value.commentCount > 0) {
        api.getComments(item.value?.id)
            .then(res => {
                comments.value = res
                commentChannel = supabase
                    .channel('comment-updates')
                    .on('postgres_changes', { 
                        event: 'INSERT', 
                        schema: 'public', 
                        table: 'comments', 
                        filter: 'post_id=eq.' + item.value?.id 
                    }, ev => {
                        if (!ev.errors) {
                            comments.value.push(ev.new)
                        }
                    })
                    .subscribe()
            })
    }
    else
    {
        comments.value = []
    }
})

api.$subscribe((mutation, state) => {
    if (state.replyingTo) replyTo.value = state.replyingTo
})

onUnmounted(() => {
    commentChannel && supabase.removeChannel(commentChannel)
})

function handleLove() {
    if (item.value?.id && typeof item.value.loveCount == 'number') {
        if (!isLoved.value) 
        {
            api.lovePost(item.value?.id).then(res => {
                if (res !== 201) {
                    isLoved.value = false
                    item.value?.loveCount && item.value.loveCount--
                }
            })
            isLoved.value = true
            item.value.loveCount++
        }
        else
        {
            api.unlovePost(item.value?.id).then(res => {
                if (res !== 204) {
                    isLoved.value = true
                    item.value?.loveCount && item.value.loveCount++
                }
            })
            isLoved.value = false
            item.value.loveCount--
        }
    }
}

function handleComment() {
    if(commentInput.value.length > 0 && item.value?.id)
    {
        api.createComment(item.value?.id, commentInput.value, replyTo.value != null ? replyTo.value.id : undefined)
            .then(res => {
                if (res === 201) {
                    console.log('commented')
                    commentInput.value = ''
                    replyTo.value = null
                }
            })
    }
}

